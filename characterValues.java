package programming;

public class characterValues {

	public static void main(String[] args) {
		char c1='a';  
		char c2='z';  
		int a=c1;  
		int b=c2;  
		System.out.println(a);  
		System.out.println(b);
		
		
		char c3='A';  
		char c4='Z';  
		int c=c3;  
		int d=c4;  
		System.out.println(c);  
		System.out.println(d);
			
	}

}
